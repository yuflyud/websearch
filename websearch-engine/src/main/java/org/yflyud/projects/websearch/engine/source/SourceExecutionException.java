package org.yflyud.projects.websearch.engine.source;

public class SourceExecutionException extends Exception {
    private static final long serialVersionUID = 1023792746433980812L;

    public SourceExecutionException() {
    }

    public SourceExecutionException(String arg0) {
        super(arg0);
    }

    public SourceExecutionException(Throwable arg0) {
        super(arg0);
    }

    public SourceExecutionException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

}
