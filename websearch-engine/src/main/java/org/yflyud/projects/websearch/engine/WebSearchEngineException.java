package org.yflyud.projects.websearch.engine;

public class WebSearchEngineException extends RuntimeException {
    private static final long serialVersionUID = -4058076235389589636L;

    public WebSearchEngineException() {
    }

    public WebSearchEngineException(String arg0) {
        super(arg0);
    }

    public WebSearchEngineException(Throwable arg0) {
        super(arg0);
    }

    public WebSearchEngineException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

}
